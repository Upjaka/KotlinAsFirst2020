 * Средняя (5 баллов)
 *
 * Результаты спортсмена на соревнованиях в прыжках в длину представлены строкой вида
 * "706 - % 717 % 703".
 * В строке могут присутствовать числа, черточки - и знаки процента %, разделённые пробелами;
 * число соответствует удачному прыжку, - пропущенной попытке, % заступу.
 * Прочитать строку и вернуть максимальное присутствующее в ней число (717 в примере).
 * При нарушении формата входной строки или при отсутствии в ней чисел, вернуть -1.
 */
fun bestLongJump(jumps: String): Int {
    val attempts = jumps.split(" ")
    var result = -1
    for (jump in attempts) {
        if (jump != "-" && jump != "%") {
            val length = checkNumber(jump, -1)
            if (length == -1) return -1 else if (length > result) result = length
        }
    }
    return result
}

fun checkNumber(str: String, default: Int): Int {
    if (str == "") return default
    for (char in str) if (char !in "1234567890") return default
    return str.toInt()
}

/**
 * Сложная (6 баллов)
 *
 * Результаты спортсмена на соревнованиях в прыжках в высоту представлены строкой вида
 * "220 + 224 %+ 228 %- 230 + 232 %%- 234 %".
 * Здесь + соответствует удачной попытке, % неудачной, - пропущенной.
 * Высота и соответствующие ей попытки разделяются пробелом.
 * Прочитать строку и вернуть максимальную взятую высоту (230 в примере).
 * При нарушении формата входной строки, а также в случае отсутствия удачных попыток,
 * вернуть -1.
 */
fun bestHighJump(jumps: String): Int {
    val attempts = jumps.split(" ")
    if (attempts.size < 2) return -1
    var result = -1
    for (i in attempts.indices) {
        if (i % 2 == 0) {
            val height = checkNumber(attempts[i], -1)
            val str = attempts[i + 1]
            for (char in str) {
                if (char !in "+-%") return -1
                if (char == '+' && height > result) result = height
            }
        }
    }
    return result
}

/**
 * Сложная (6 баллов)
 *
 * В строке представлено выражение вида "2 + 31 - 40 + 13",
 * использующее целые положительные числа, плюсы и минусы, разделённые пробелами.
 * Наличие двух знаков подряд "13 + + 10" или двух чисел подряд "1 2" не допускается.
 * Вернуть значение выражения (6 для примера).
 * Про нарушении формата входной строки бросить исключение IllegalArgumentException
 */
fun plusMinus(expression: String): Int {
    val parts = expression.split(" ")
    var result = checkNumber(parts[0])
    for (i in parts.indices) {
        if (i % 2 == 1) {
            if (i == parts.size - 1) throw IllegalArgumentException()
            when (parts[i]) {
                "+" -> result += checkNumber(parts[i + 1])
                "-" -> result -= checkNumber(parts[i + 1])
                else -> throw IllegalArgumentException()
            }
        }
    }
    return result
}

fun checkNumber(str: String): Int {
    if (str == "") throw IllegalArgumentException()
    for (char in str) if (char !in "1234567890") throw IllegalArgumentException()
    return str.toInt()
}

/**
 * Сложная (6 баллов)
 *
 * Строка состоит из набора слов, отделённых друг от друга одним пробелом.
 * Определить, имеются ли в строке повторяющиеся слова, идущие друг за другом.
 * Слова, отличающиеся только регистром, считать совпадающими.
 * Вернуть индекс начала первого повторяющегося слова, или -1, если повторов нет.
 * Пример: "Он пошёл в в школу" => результат 9 (индекс первого 'в')
 */
fun firstDuplicateIndex(str: String): Int {
    val words = str.toLowerCase().split(' ')
    if (words.size < 2) return -1
    var result = 0
    for (i in 1 until words.size) {
        if (words[i] == words[i - 1]) {
            for (j in 0 until i - 1) {
                result += words[j].length + 1
            }
            return result
        }
    }
    return -1
}

/**
 * Сложная (6 баллов)
 *
 * Строка содержит названия товаров и цены на них в формате вида
 * "Хлеб 39.9; Молоко 62; Курица 184.0; Конфеты 89.9".
 * То есть, название товара отделено от цены пробелом,
 * а цена отделена от названия следующего товара точкой с запятой и пробелом.
 * Вернуть название самого дорогого товара в списке (в примере это Курица),
 * или пустую строку при нарушении формата строки.
 * Все цены должны быть больше нуля либо равны нулю.
 */
fun mostExpensive(description: String): String {
    var result = Pair("", 0.0)
    val goods = description.split("; ")
    for (good in goods) {
        if (good != "") {
            val name = good.split(' ')[0]
            val cost = good.split(' ')[1].toDouble()
            if (cost >= result.second) result = Pair(name, cost)
        }
    }
    return result.first
}

/**
 * Сложная (6 баллов)
 *
 * Перевести число roman, заданное в римской системе счисления,
 * в десятичную систему и вернуть как результат.
 * Римские цифры: 1 = I, 4 = IV, 5 = V, 9 = IX, 10 = X, 40 = XL, 50 = L,
 * 90 = XC, 100 = C, 400 = CD, 500 = D, 900 = CM, 1000 = M.
 * Например: XXIII = 23, XLIV = 44, C = 100
 *
 * Вернуть -1, если roman не является корректным римским числом
 */
fun fromRoman(roman: String): Int {
    var result = 0
    val letters = mapOf("I" to 1, "V" to 5, "X" to 10, "L" to 50, "C" to 100, "D" to 500, "M" to 1000)
    val pairs = mapOf("IV" to 4, "IX" to 9, "XL" to 40, "XC" to 90, "CD" to 400, "CM" to 900)
    if (roman == "") return -1
    if (roman.length == 1) return letters.getOrDefault(roman, -1)
    var i = 0
    val pair = pairs[roman[i].toString() + roman[i + 1].toString()]
    if (pair != null) {
        result += pair
        i = 2
    } else {
        val letter = letters[roman[i].toString()]
        if (letter != null) {
            result += letter
            i = 1
        } else return -1
    }
    while (i < roman.length - 1) {
        val withNext = pairs[roman[i].toString() + roman[i + 1].toString()]
        val withEarly = pairs[roman[i - 1].toString() + roman[i].toString()]
        if (withEarly == null) {
            result += withNext ?: (letters[roman[i].toString()] ?: return -1)
            if (i == roman.length - 2 && withNext == null) if (letters[roman[i + 1].toString()] == null) return -1 else
                result += letters[roman[i + 1].toString()]!!
        }
        i++
    }
    return result
}

