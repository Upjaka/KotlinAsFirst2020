/**
 * Средняя (4 балла)
 *
 * Номер телефона задан строкой вида "+7 (921) 123-45-67".
 * Префикс (+7) может отсутствовать, код города (в скобках) также может отсутствовать.
 * Может присутствовать неограниченное количество пробелов и чёрточек,
 * например, номер 12 --  34- 5 -- 67 -89 тоже следует считать легальным.
 * Перевести номер в формат без скобок, пробелов и чёрточек (но с +), например,
 * "+79211234567" или "123456789" для приведённых примеров.
 * Все символы в номере, кроме цифр, пробелов и +-(), считать недопустимыми.
 * При неверном формате вернуть пустую строку.
 *
 * PS: Дополнительные примеры работы функции можно посмотреть в соответствующих тестах.
 */
fun flattenPhoneNumber(phone: String): String {
    val digits = "1234567890"
    val result = StringBuilder()
    val country: String
    val city: String
    val number: String

    val matchResult = Regex("""(^\+[1234567890]*)?( *\([ 1234567890-]*\))?([ 1234567890-]+)""")
        .find(phone)
    if (matchResult == null) return "" else {
        country = matchResult.groupValues[1]
        city = matchResult.groupValues[2]
        number = matchResult.groupValues[3]
    }
    if (country + city + number != phone) return ""
    var flag = false
    if (country != "") {
        result.append('+')
        for (char in country) {
            if (char in digits) {
                result.append(char)
                flag = true
            }
        }
        if (!flag) return ""
    }
    if (city != "") {
        flag = false
        for (char in city) {
            if (char in digits) {
                result.append(char)
                flag = true
            }
        }
        if (!flag) return ""
    }
    if (number != "") {
        flag = false
        for (char in number) {
            if (char in digits) {
                result.append(char)
                flag = true
            }
        }
        if (!flag) return ""
    }
    return result.toString()
}

