        return ""
    }
    val month = try {
        parts[1].toInt()
    } catch (e: NumberFormatException) {
        return ""
    }
    val monthStr = when (parts[1]) {
        "01" -> "января"
        "02" -> "февраля"
        "03" -> "марта"
        "04" -> "апреля"
        "05" -> "мая"
        "06" -> "июня"
